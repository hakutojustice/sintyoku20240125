void Map::Draw()
{

	for (int x = 0; x < kChipNumX; x++)
	{
		for (int y = 0; y < kChipNumY; y++)
		{
			//何も置かない部分は飛ばす
			if (kChipData[y][x] == 0)	continue;

			int posX = kChipW * x;
			int posY = kChipH * y;

			DrawBox(posX, posY, posX + kChipW, posY + kChipH, 0xffffff, true);


		}
	}
}
